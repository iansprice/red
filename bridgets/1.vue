function logScale(value: number) {
  // Handle edge cases
  if (value <= 0) return 1;
  if (value >= 100) return max;

  // Calculate log scale
  // Using Math.exp() to create exponential curve
  const minp = 0;
  const maxp = 100;
  const minv = Math.log(1);  // Log of 1 = 0
  const maxv = Math.log(max + 1);  // Using 2501 to make max exactly 2500

  // Calculate scale
  const scale = (maxv - minv) / (maxp - minp);

  return Math.round(Math.exp(minv + scale * (value - minp)));
}

watch(
    [group, delay],
    ([g, d]: [SVGGElement, number]) => {
